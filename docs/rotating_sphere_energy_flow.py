def compute_energy_flow(r, omega, m=1.0):
    """
    计算旋转小球的能量分布
    
    r: 半径
    omega: 角速度
    m: 质量
    """
    # 向心力做的功
    F_c = m * omega**2 * r  # 向心力
    W_c = 0.5 * m * (omega * r)**2  # 动能 = 向心力做的功
    
    # 外空间能量（表象）
    E_outer = W_c
    
    # 内空间能量（实际存储）
    # 能量流入内空间后展开为10维
    E_inner = W_c * (10/4)  # 维度比
    
    # 表观谱维（外空间）
    # 随着能量流入内空间，外空间表观维度降低
    d_s_outer = 4 - 2 * (E_inner / (E_outer + E_inner))
    
    # 内空间谱维
    d_s_inner = 4 + 6 * (E_inner / (E_outer + E_inner))
    
    return {
        'F_c': F_c,
        'W_c': W_c,
        'E_outer': E_outer,
        'E_inner': E_inner,
        'd_s_outer': d_s_outer,
        'd_s_inner': d_s_inner
    }

# 计算不同旋转速度的情况
omegas = np.linspace(0, 100, 100)  # 角速度范围
r = 1.0  # 半径

results = [compute_energy_flow(r, w) for w in omegas]

