# 计算能量分配
def compute_energy_distribution(r_over_rs, tau_0=0.005):
    """
    计算能量在外空间和内空间的分配
    """
    if r_over_rs >= 1:
        # 外空间（包括视界处）
        # 外空间能量比例随r减小而降低
        f_4D = 1 / (1 + tau_0 * (1/r_over_rs - 1))
        f_4D = max(0, min(1, f_4D))
    else:
        # 严格来说，r < r_s 已经进入内空间拓扑
        # 这里简化处理
        f_4D = 0
    
    f_10D = 1 - f_4D
    
    return {'f_4D': f_4D, 'f_10D': f_10D}

