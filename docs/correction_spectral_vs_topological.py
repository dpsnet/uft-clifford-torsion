# 计算谱维随半径变化
def compute_spectral_dimension(r_over_rs, tau_0=0.005):
    """
    计算谱维随半径的变化
    拓扑维度始终是4，变化的是谱维（有效维度）
    """
    if r_over_rs > 1:
        # 远离黑洞
        d_s = 4
    elif r_over_rs == 1:
        # 视界处
        d_s = 7
    else:
        # 视界内（但仍在外空间拓扑中）
        # 局部能量密度 ~ 1/r
        tau_eff = tau_0 / r_over_rs
        d_s = 4 + 6 * tau_eff / (1 + tau_eff)
        d_s = min(d_s, 10)
    
    return d_s

