# 计算内空间坐标
def compute_inner_space_expansion(r, r_s, tau_0=0.005):
    """
    计算内空间扩张
    
    r: 外空间径向坐标
    r_s: 史瓦西半径
    tau_0: 扭转场强度
    """
    # 内空间径向坐标
    r_int = r_s * (r_s / r) ** tau_0
    
    # 局部扭转场
    tau_local = tau_0 * (r_s / r)
    
    # 局部分形维数
    D_f = 4 + 6 * tau_local / (1 + tau_local)
    
    # 局部谱维
    d_s = min(D_f, 10)  # 上限10维
    
    return {
        'r_int': r_int,
        'tau': tau_local,
        'D_f': D_f,
        'd_s': d_s
    }

