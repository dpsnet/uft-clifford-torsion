# 计算耦合常数的能量依赖
def compute_coupling(E, E_GUT=1e16, alpha_0=1/25):
    """
    简化的耦合常数跑动
    """
    # 对数跑动
    beta = 0.01  # beta函数系数（简化）
    alpha_inv = 1/alpha_0 + beta * np.log(E/E_GUT)
    return 1/alpha_inv

