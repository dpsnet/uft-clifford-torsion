def compute_dimension_change(r_over_rs):
    """
    计算维度随半径的变化
    
    r_over_rs: r/r_s 比值
    """
    tau_0 = 0.005
    
    # 外空间有效维度
    if r_over_rs > 1:
        d_s_outer = 4
        d_time = 1  # 1个时间维
        d_space = 3  # 3个空间维
        d_hidden = 0
    elif r_over_rs == 1:
        d_s_outer = 7  # 视界处
        d_time = 1
        d_space = 3
        d_hidden = 3  # 开始显现
    else:
        # 视界内
        tau = tau_0 / r_over_rs  # 随深度增加
        d_s_outer = 4 + 6 * tau / (1 + tau)
        d_s_outer = min(d_s_outer, 10)
        
        # 维度重新分配
        d_time = 1 + 3 * (d_s_outer - 4) / 6  # 时间维增加
        d_space = 3 + 3 * (d_s_outer - 4) / 6  # 空间维也增加
        d_hidden = d_s_outer - 4
    
    return {
        'd_s': d_s_outer,
        'd_time': d_time,
        'd_space': d_space,
        'd_hidden': d_hidden
    }

