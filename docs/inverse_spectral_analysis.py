# 实现逆向推导
def compute_torsion_from_spectral(d_s):
    """
    从谱维计算扭转场
    """
    if d_s <= 4:
        return 0
    elif d_s >= 10:
        return float('inf')
    else:
        return (d_s - 4) / (10 - d_s)

def compute_inner_position_forward(r, r_s, tau_0=0.005):
    """
    正向：从外空间坐标计算内空间坐标
    """
    if r <= 0:
        return float('inf')
    return r_s * (r_s / r) ** tau_0

def compute_inner_position_inverse(tau, r, r_s, tau_0=0.005, delta=0.1, beta=1.0):
    """
    逆向：从扭转场推断内空间坐标
    
    tau: 测量的扭转场
    r: 外空间测量位置
    r_s: 史瓦西半径
    tau_0: 特征扭转强度
    delta, beta: 内空间几何参数
    """
    if tau <= tau_0 * (r_s / r):
        return r_s  # 最小值
    
    # 简化的逆向公式
    # tau = tau_0 * (r_s/r) * (1 + delta * (r_int/r_s)^{-beta})
    # 解出 r_int
    
    geometric_factor = tau / (tau_0 * (r_s / r))
    if geometric_factor <= 1:
        return r_s
    
    r_int = r_s * ((geometric_factor - 1) / delta) ** (-1/beta)
    return r_int

