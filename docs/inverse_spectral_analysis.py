# 真实内空间形状（假设是椭球）
def true_inner_shape(theta, phi):
    a, b, c = 2.0, 1.5, 1.0  # 椭球半轴（以r_s为单位）
    r = 1 / np.sqrt((np.sin(theta)*np.cos(phi)/a)**2 + 
                    (np.sin(theta)*np.sin(phi)/b)**2 + 
                    (np.cos(theta)/c)**2)
    return r

# 生成"测量"的谱维数据
def generate_spectral_data(theta, phi, noise_level=0.01):
    r_int_true = true_inner_shape(theta, phi)
    # 简化的谱维-内空间关系
    d_s = 4 + 6 * (1 - 1/r_int_true) / (1 + 1/r_int_true)
    d_s += np.random.normal(0, noise_level)
    return np.clip(d_s, 4, 10)

# 重建内空间
def reconstruct_inner_space(theta_grid, phi_grid, d_s_grid):
    """
    从谱维数据重建内空间坐标
    """
    r_int_reconstructed = np.zeros_like(d_s_grid)
    
    for i in range(len(theta_grid)):
        for j in range(len(phi_grid)):
            d_s = d_s_grid[i, j]
            tau = (d_s - 4) / (10 - d_s) if d_s < 9.9 else 10
            
            # 简化的逆向映射
            # 假设 tau ~ 1/r_int
            if tau > 0.1:
                r_int_reconstructed[i, j] = 1.0 / tau
            else:
                r_int_reconstructed[i, j] = 10.0
    
    return r_int_reconstructed

